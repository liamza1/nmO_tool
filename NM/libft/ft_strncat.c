#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		q;

	i = 0;
	q = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[q] != '\0' && q < n)
	{
		s1[i] = s2[q];
		i++;
		q++;
	}
	s1[i] = '\0';
	return (s1);
}
